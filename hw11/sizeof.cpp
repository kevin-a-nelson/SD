#include<iostream>

using namespace std;

int main(){

    cout << "sizeof(short)\t" << sizeof(short) << endl;
    cout << "sizeof(int)\t" << sizeof(int) << endl;
    cout << "sizeof(double)\t" << sizeof(double) << endl;
    cout << "sizeof(*short)\t" << sizeof(double) << endl;
    cout << "sizeof(*int)\t" << sizeof(double) << endl;
    cout << "sizeof(*double)\t" << sizeof(double) << endl;

    return 0;
}